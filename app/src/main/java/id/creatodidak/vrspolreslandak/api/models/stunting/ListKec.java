package id.creatodidak.vrspolreslandak.api.models.stunting;

public class ListKec {
    private int id;
    private int prov;
    private int kab;
    private String nama;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getProv() {
        return prov;
    }

    public void setProv(int prov) {
        this.prov = prov;
    }

    public int getKab() {
        return kab;
    }

    public void setKab(int kab) {
        this.kab = kab;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
}
